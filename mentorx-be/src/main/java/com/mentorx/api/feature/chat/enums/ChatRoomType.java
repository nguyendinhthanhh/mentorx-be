package com.mentorx.api.feature.chat.enums;

/**
 * Enum representing different types of chat rooms
 * 
 * @author MentorX Development Team
 * @since 1.0.0
 */
public enum ChatRoomType {
    /**
     * Direct message between two users
     */
    DIRECT_MESSAGE,
    
    /**
     * Group chat for project collaboration
     */
    PROJECT_GROUP,
    
    /**
     * Course discussion room
     */
    COURSE_DISCUSSION,
    
    /**
     * Quick support session
     */
    QUICK_SUPPORT,
    
    /**
     * Scheduled mentoring session
     */
    MENTORING_SESSION,
    
    /**
     * Community discussion room
     */
    COMMUNITY_ROOM,
    
    /**
     * Support ticket conversation
     */
    SUPPORT_TICKET
}